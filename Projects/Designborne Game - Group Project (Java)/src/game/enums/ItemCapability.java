/**
 * An enumeration representing different item capabilities in the game.
 */
public enum ItemCapability {
    /**
     * Represents an old key item capability.
     */
    UNLOCKABLE,

    /**
     * Represents a healing item capability.
     */
    HEALING,

    /**
     * Represents an item that has a tradeable capability.
     */
    TRADEABLE,

    /**
     * Represents an item that has a upgradable capability.
     */
    UPGRADABLE
}
