/**
 * An enumeration representing different types of actors in the game.
 */
public enum ActorType {
    /**
     * Represents the playable character controlled by the player.
     */
    PLAYABLE,

    /**
     * Represents an enemy actor type - Wandering Undead.
     */
    ENEMY_WANDERING_UNDEAD,

    /**
     * Represents an enemy actor type - Hollow Soldier.
     */
    ENEMY_HOLLOW_SOLDIER,

    /**
     * Represents an enemy actor type - Forest Keeper.
     */
    ENEMY_FOREST_KEEPER,

    /**
     * Represents an enemy actor type - Red Wolf.
     */
    ENEMY_RED_WOLF,

    /**
     * Represents an entity type - Trader.
     */
    TRADER,

    /**
     * Represents an enemy boss!!
     */
    ENEMY_BOSS,
    ENEMY_ELDENTREE_GUARDIAN,
    ENEMY_LIVING_BRANCH

}
